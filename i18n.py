def set_language(lang_code):
    global CURRENT_LANG
    if lang_code in ["de", "en", "fr", "es"]:
        CURRENT_LANG = lang_code

def get_language():
    return CURRENT_LANG

def tr(text):
    if CURRENT_LANG == "de":
        return text
    
    if CURRENT_LANG in TRANSLATIONS and text in TRANSLATIONS[CURRENT_LANG]:
        return TRANSLATIONS[CURRENT_LANG][text]
    
    # Fallback
    for k, v in TRANSLATIONS[CURRENT_LANG].items():
        if text.startswith(k) and len(text) > len(k):
            return text.replace(k, v)
            
    return text

# A helper to wrap strings directly in code
def _(text):
    return tr(text)

# We install this into builtins so all files can use _("text") without importing
import builtins
builtins._ = _
